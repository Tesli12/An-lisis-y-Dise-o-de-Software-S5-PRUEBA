    public void Guardardatos(){
        ArrayList listaprod=new  ArrayList();
        TicketDCompra tick=new TicketDCompra();
        float calcula,precio;
       
        for(int i=0;i<jTablaVentas.getRowCount();i++){
           tick.setFolio(Integer.parseInt(TxtFolio.getText().trim()));
           tick.setNumeroArt(Integer.parseInt((String)jTablaVentas.getValueAt(i, 1)) );
           tick.setArticulos((String)jTablaVentas.getValueAt(i, 2));
           precio=(float) jTablaInv.getValueAt(i, 3);
           calcula =tick.getNumeroArt() * precio;
           tick.setTotal(calcula);
           listaprod.add(tick);
           ArchivoProdTicket.aniadirArchivo(listaprod);
           listaprod=new ArrayList();
        }
        
        
    }
    
    
