    private void editarBTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBTONActionPerformed
        ArrayList listamod =new ArrayList();
        
        for(Cliente c:(ArrayList<Cliente>)listaLeida){
            
          if(c.getFolio()==(Integer.parseInt(TFol.getText()))){
              c.setNombre(TNomb.getText());
              c.setApellidoPaterno(TAP.getText());
              c.setApellidoMaterno(TAM.getText());
              c.setTelefono(Integer.parseInt(TTel.getText()));
              c.setCorreoElectronico(TCor.getText());
              c.setGeneral((String)jComboBox1.getSelectedItem());
                
          }
          System.out.println("sdfsdf:"+c.getApellidoPaterno());
            listamod.add(c);
      
      }

        ArchivoClientes.Modificar(listamod);
        LimpiarTabla();
        Datos();
    }//GEN-LAST:event_editarBTONActionPerformed

