    private void jBEliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminaActionPerformed
        // TODO add your handling code here:
        listaLeida = ArchivoClientes.leerArchivo();
        int resp,fsel,id;
        fsel=jTTablaCliente.getSelectedRow();
        if(fsel== -1){
        
         JOptionPane.showMessageDialog(null, "Selecione una fila ","Error",JOptionPane.WARNING_MESSAGE);
        }
        else {
            resp=JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea eliminar este producto?","Elminar",
                        JOptionPane.YES_NO_OPTION);
            
            if(resp==JOptionPane.YES_OPTION){
                id=((int)jTTablaCliente.getValueAt(fsel,5));
                for(Cliente cliente:(ArrayList<Cliente>)listaLeida){
                    if(cliente.getFolio()==id){
                        listaLeida.remove(cliente);
                        ArchivoClientes.crearArchivos(listaLeida);
                        break;
                    }
                }
                JOptionPane.showMessageDialog(null, " Fila eliminada  ");
                modelo.removeRow(jTTablaCliente.getSelectedRow());
                listaClientes.indexOf(jTTablaCliente.getSelectedRow());
            }
        }
    }//GEN-LAST:event_jBEliminaActionPerformed

